#include <iostream>
#include <math.h>
#include <cstdio>
#include <fstream>
#include <vector>
#include "hardware_process_helper.h"
#include "halide_image_io.h"
#include "coreir.h"
#include "hw_support_utils.h"
#include "cstdlib"

#if defined(WITH_CPU)
   #include "zircon_scale_add_fp.h"
#endif

#if defined(WITH_COREIR)
    #include "coreir_interpret.h"
#endif

#if defined(WITH_CLOCKWORK)
    #include "rdai_api.h"
    #include "clockwork_sim_platform.h"
    #include "zircon_scale_add_fp_clockwork.h"
#endif

using namespace Halide::Tools;
using namespace Halide::Runtime;

int main( int argc, char **argv ) {
  std::map<std::string, std::function<void()>> functions;
  ManyInOneOut_ProcessController<uint16_t> processor("zircon_scale_add_fp", {"mu_hw_input_stencil.mat", "hw_attn_mask_input_stencil.raw"});

  #if defined(WITH_CPU)
      auto cpu_process = [&]( auto &proc ) {
      zircon_scale_add_fp(proc.inputs["mu_hw_input_stencil.mat"], proc.inputs["hw_attn_mask_input_stencil.raw"], proc.output);
      };
      functions["cpu"] = [&](){ cpu_process( processor ); } ;
  #endif

  #if defined(WITH_COREIR)
      auto coreir_process = [&]( auto &proc ) {
          run_coreir_on_interpreter<>( "bin/design_top.json",
                                       proc.inputs["mu_hw_input_stencil.mat"], proc.output,
                                       "self.in_arg_0_0_0", "self.out_0_0" );
      };
      functions["coreir"] = [&](){ coreir_process( processor ); };
  #endif

  #if defined(WITH_CLOCKWORK)
      auto clockwork_process = [&]( auto &proc ) {
        RDAI_Platform *rdai_platform = RDAI_register_platform( &rdai_clockwork_sim_ops );
        if ( rdai_platform ) {
          printf( "[RUN_INFO] found an RDAI platform\n" );
          zircon_scale_add_fp_clockwork( proc.inputs["mu_hw_input_stencil.mat"], proc.inputs["hw_attn_mask_input_stencil.raw"], proc.output );
          RDAI_unregister_platform( rdai_platform );
        } else {
          printf("[RUN_INFO] failed to register RDAI platform!\n");
        }
      };
      functions["clockwork"] = [&](){ clockwork_process( processor ); };
  #endif

  // Add all defined functions
  processor.run_calls = functions;

  processor.inputs_preset = true;


  auto N = getenv("seq_len");
  auto seq_len = N ? atoi(N) : 64;

  auto ATTN_SCALE = getenv("ATTN_SCALE");
  const float attn_scale = ATTN_SCALE ? atof(ATTN_SCALE) : 0.5f;
  printf("Using attn_scale of %f\n", attn_scale);

  processor.inputs["mu_hw_input_stencil.mat"] = Buffer<uint16_t>(seq_len, seq_len);
  processor.inputs["hw_attn_mask_input_stencil.raw"] = Buffer<uint16_t>(seq_len, seq_len);
  processor.output  = Buffer<uint16_t>(seq_len, seq_len);

  int max_rand = 256 - 1;
  // Fill input with random values
  for (int x = 0; x < processor.inputs["mu_hw_input_stencil.mat"].dim(0).extent(); x++) {
      for (int y = 0; y < processor.inputs["mu_hw_input_stencil.mat"].dim(1).extent(); y++) {
        processor.inputs["mu_hw_input_stencil.mat"](x, y) = float_to_bfloat16_process(
            // [-7, 7]
            ((float)rand() / RAND_MAX) * 14.0 - 7.0
          );
    }
  }

   // Fill input with random values
  for (int x = 0; x < processor.inputs["hw_attn_mask_input_stencil.raw"].dim(0).extent(); x++) {
      for (int y = 0; y < processor.inputs["hw_attn_mask_input_stencil.raw"].dim(1).extent(); y++) {
        processor.inputs["hw_attn_mask_input_stencil.raw"](x, y) = float_to_bfloat16_process(
            // [-7, 7]
            ((float)rand() / RAND_MAX) * 14.0 - 7.0
          );
    }
  }

  // Gold output
  for (int y = 0; y < processor.output.dim(1).extent(); y++) {
    for (int x = 0; x <  processor.output.dim(0).extent(); x++) {
        float sum = (bfloat16_to_float_process(processor.inputs["mu_hw_input_stencil.mat"](x, y)) * attn_scale) + bfloat16_to_float_process(processor.inputs["hw_attn_mask_input_stencil.raw"](x, y));
        processor.output(x, y) = float_to_bfloat16_process(sum);
      }
    }

  std::cout << "Writing mu_hw_input_stencil.mat to bin folder" << std::endl;
  save_image(processor.inputs["mu_hw_input_stencil.mat"], "bin/mu_hw_input_stencil.mat");

  // std::cout << "Writing hw_attn_mask_input_stencil.raw to bin folder" << std::endl;
  save_halide_buffer_to_raw(processor.inputs["hw_attn_mask_input_stencil.raw"], "bin/hw_attn_mask_input_stencil.raw");

  // Copy the pre-existing raw file into the bin folder for now
  // int ret = system("cp hw_attn_mask_input_stencil.raw bin/hw_attn_mask_input_stencil.raw");
  // if (ret != 0) {
  //   std::cerr << "Error: Failed to copy hw_attn_mask_input_stencil.raw to bin folder. "
  //                "hw_attn_mask_input_stencil.raw should have been generated by /aha/voyager/scripts/aha_flow.parse_dnnLayer_tensors.py "
  //                "(system call returned " << ret << ")" << std::endl;
  //   return 1;
  // }
  // std::cout << "Copying pre-existing hw_attn_mask_input_stencil.raw to bin folder" << std::endl;

  std::cout << "Writing output to bin folder" << std::endl;
  save_image(processor.output, "bin/hw_output.mat");

  return processor.process_command(argc, argv);
}
