#include "hardware_process_helper.h"
#include "halide_image_io.h"
#include "hw_support_utils.h"


#if defined(WITH_CPU)
#include "get_e8m0_scale_accum_gb_input.h"
#endif

#if defined(WITH_COREIR)
#include "coreir_interpret.h"
#endif

#if defined(WITH_CLOCKWORK)
#include "get_e8m0_scale_accum_gb_input_clockwork.h"
#include "clockwork_sim_platform.h"
#include "rdai_api.h"
#endif

using namespace Halide::Tools;
using namespace Halide::Runtime;

int main(int argc, char **argv) {
    std::map<std::string, std::function<void()>> functions;
    ManyInOneOut_ProcessController<uint16_t> processor("get_e8m0_scale_accum_gb_input", { "input" });

#if defined(WITH_CPU)
    auto cpu_process = [&](auto &proc) {
        get_e8m0_scale_accum_gb_input(proc.inputs["input"], proc.output);
    };
    functions["cpu"] = [&]() {
        cpu_process(processor);
    };
#endif

#if defined(WITH_COREIR)
    auto coreir_process = [&](auto &proc) {
        run_coreir_on_interpreter<>("bin/design_top.json",
                                    proc.inputs["input"], proc.output,
                                    "self.in_arg_0_0_0", "self.out_0_0");
    };
    functions["coreir"] = [&]() {
        coreir_process(processor);
    };
#endif

#if defined(WITH_CLOCKWORK)
    auto clockwork_process = [&](auto &proc) {
        RDAI_Platform *rdai_platform = RDAI_register_platform(&rdai_clockwork_sim_ops);
        if (rdai_platform) {
            printf("[RUN_INFO] found an RDAI platform\n");
            get_e8m0_scale_accum_gb_input_clockwork(proc.inputs["input"], proc.output);
            RDAI_unregister_platform(rdai_platform);
        } else {
            printf("[RUN_INFO] failed to register RDAI platform!\n");
        }
    };
    functions["clockwork"] = [&]() {
        clockwork_process(processor);
    };
#endif

    processor.run_calls = functions;

    auto head_dim_fake_env = getenv("head_dim_fake");
    auto seq_heads_prod_fake_env = getenv("seq_heads_prod_fake");
    auto head_dim_env = getenv("head_dim");
    auto seq_heads_prod_env = getenv("seq_heads_prod");

    auto head_dim_fake = head_dim_fake_env ? atoi(head_dim_fake_env) : 64;
    auto seq_heads_prod_fake = seq_heads_prod_fake_env ? atoi(seq_heads_prod_fake_env) : 128;
    auto head_dim = head_dim_env ? atoi(head_dim_env) : 64;
    auto seq_heads_prod = seq_heads_prod_env ? atoi(seq_heads_prod_env) : 128;

    std::cout << "using inputs set within process.cpp" << std::endl;
    processor.inputs_preset = true;

    // Input
    processor.inputs["input"] = Buffer<uint16_t>(head_dim_fake, seq_heads_prod_fake);
    auto real_input = Buffer<uint16_t>(head_dim, seq_heads_prod);
    for (int s = 0; s < real_input.dim(1).extent(); s++) {
        for (int d = 0; d < real_input.dim(0).extent(); d++) {
            real_input(d, s) = float_to_bfloat16_process(static_cast<float>((rand() / (float) RAND_MAX) * 2048.0f - 1024.0f));
        }
    }

    // Gold output
    const int block_size = 64;
    processor.output = Buffer<uint16_t>(head_dim_fake / 2, seq_heads_prod_fake / block_size);
    // processor.output = Buffer<uint16_t>(head_dim_fake, seq_heads_prod_fake / block_size);
    auto real_output = Buffer<uint16_t>(head_dim / 2, seq_heads_prod / block_size);
    for (int s = 0; s < real_output.dim(1).extent(); s++) {
        const int base = s * block_size;
        for (int d_pack = 0; d_pack < real_output.dim(0).extent(); d_pack++) {
            const int d0 = 2 * d_pack;
            const int d1 = d0 + 1;

            uint16_t max0 = real_input(d0, base);
            uint16_t max1 = real_input(d1, base);
            for (int r = 1; r < block_size; r++) {
                max0 = abs_max(max0, real_input(d0, base + r));
                max1 = abs_max(max1, real_input(d1, base + r));
            }

            const uint16_t scale0 = get_shared_exp(max0);
            const uint16_t scale1 = get_shared_exp(max1);
            real_output(d_pack, s) = bit8_pack(scale1, scale0);
        }
    }

    auto voyager_gold_env = getenv("VOYAGER_GOLD");
    bool use_random_tensors = true;
    if (voyager_gold_env && strcmp(voyager_gold_env, "1") == 0) {
        use_random_tensors = false;
    }
    if (use_random_tensors) {
        // Use random tensors
        std::cout << "Generating random tensors" << std::endl;
        save_halide_buffer_to_raw(real_input, "bin/input_host_stencil.raw");
        save_halide_buffer_to_raw(real_output, "bin/hw_output.raw");
    } else {
        // Copy the pre-existing raw file into the bin folder for now
        int ret = system("cp hw_input_stencil.raw bin/input_host_stencil.raw");
        if (ret != 0) {
            std::cerr << "Error: Failed to copy hw_input_stencil.raw to bin folder. "
                        "hw_input_stencil.raw should have been generated by /aha/voyager/scripts/aha_flow/parse_dnnLayer_tensors.py "
                        "(system call returned " << ret << ")" << std::endl;
            return 1;
        }
        std::cout << "Copying pre-existing hw_input_stencil.raw to bin/input_host_stencil.raw" << std::endl;
        // throw std::runtime_error("Voyager gold not supported yet");

        std::cout << "Writing output to bin folder" << std::endl;
        save_halide_buffer_to_raw(real_output, "bin/hw_output.raw");
    }

    // Create glb bank config
    using namespace glb_cfg;
    const config_spec spec = {
        {tensor_spec{"input_host_stencil", {"x_coord"}}},
        {tensor_spec{"hw_output", {"x_coord"}}}
    };
    write_glb_bank_config(spec);

    auto output = processor.process_command(argc, argv);

    return output;
}